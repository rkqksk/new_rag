def track_qa_request(service_type: str = "sync"):
    """
    Decorator to track Q&A request metrics

    Usage:
        @track_qa_request(service_type="async")
        async def answer_question(...):
            ...
    """
    def decorator(func: Callable):
        @wraps(func)
        async def async_wrapper(*args, **kwargs):
            qa_requests_total.labels(service_type=service_type).inc()

            start_time = time.time()
            try:
                result = await func(*args, **kwargs)
                duration = time.time() - start_time
                qa_request_duration_seconds.labels(service_type=service_type).observe(duration)

                # Track confidence if available
                if isinstance(result, dict) and 'confidence' in result:
                    qa_confidence_score.observe(result['confidence'])

                return result
            except Exception as e:
                errors_total.labels(
                    error_type=type(e).__name__,
                    component='qa_service'
                ).inc()
                raise

        @wraps(func)
        def sync_wrapper(*args, **kwargs):
            qa_requests_total.labels(service_type=service_type).inc()

            start_time = time.time()
            try:
                result = func(*args, **kwargs)
                duration = time.time() - start_time
                qa_request_duration_seconds.labels(service_type=service_type).observe(duration)

                # Track confidence if available
                if isinstance(result, dict) and 'confidence' in result:
                    qa_confidence_score.observe(result['confidence'])

                return result
            except Exception as e:
                errors_total.labels(
                    error_type=type(e).__name__,
                    component='qa_service'
                ).inc()
                raise

        # Return appropriate wrapper based on function type
        import asyncio
        if asyncio.iscoroutinefunction(func):
            return async_wrapper
        return sync_wrapper

    return decorator


def track_qdrant_search():
    """Decorator to track Qdrant search metrics"""
    def decorator(func: Callable):
        @wraps(func)
        async def async_wrapper(*args, **kwargs):
            start_time = time.time()
            try:
                results = await func(*args, **kwargs)
                duration = time.time() - start_time
                qdrant_search_duration_seconds.observe(duration)

                # Track result count
                if isinstance(results, list):
                    qdrant_search_results_count.observe(len(results))

                return results
            except Exception as e:
                errors_total.labels(
                    error_type=type(e).__name__,
                    component='qdrant'
                ).inc()
                raise

        return async_wrapper

    return decorator


def track_llm_request(model: str):
    """Decorator to track LLM request metrics"""
    def decorator(func: Callable):
        @wraps(func)
        async def async_wrapper(*args, **kwargs):
            start_time = time.time()
            try:
                result = await func(*args, **kwargs)
                duration = time.time() - start_time

                llm_request_duration_seconds.labels(model=model).observe(duration)
                llm_requests_total.labels(model=model, status='success').inc()

                # Track tokens if available
                if isinstance(result, dict) and 'tokens' in result:
                    llm_tokens_generated.labels(model=model).observe(result['tokens'])

                return result
            except Exception as e:
                llm_requests_total.labels(model=model, status='error').inc()
                errors_total.labels(
                    error_type=type(e).__name__,
                    component='llm'
                ).inc()
                raise

        return async_wrapper

    return decorator


