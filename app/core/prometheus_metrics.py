def increment_active_requests():
    """Increment active requests counter"""
    active_requests.inc()


def decrement_active_requests():
    """Decrement active requests counter"""
    active_requests.dec()


def set_connection_pool_size(pool_type: str, size: int):
    """Set connection pool size gauge"""
    connection_pool_size.labels(pool_type=pool_type).set(size)


def record_embedding_duration(duration: float):
    """Record embedding generation duration"""
    embedding_generation_duration_seconds.observe(duration)


def record_error(error_type: str, component: str):
    """Record an error"""
    errors_total.labels(error_type=error_type, component=component).inc()


