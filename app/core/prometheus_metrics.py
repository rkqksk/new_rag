def metrics_endpoint() -> Response:
    """
    Generate Prometheus metrics endpoint response

    Usage in FastAPI:
        @app.get("/metrics")
        def get_metrics():
            return metrics_endpoint()
    """
    metrics_data = generate_latest(registry)
    return Response(
        content=metrics_data,
        media_type=CONTENT_TYPE_LATEST
    )


